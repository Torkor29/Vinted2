// Map proxy -> UA for session stickiness
const proxyUaMap = new Map<string, string>();

export function getRandomUserAgent(): string {
  const index = Math.floor(Math.random() * USER_AGENTS.length);
  return USER_AGENTS[index]!;
}

export function getUserAgentForProxy(proxyKey: string): string {
  let ua = proxyUaMap.get(proxyKey);
  if (!ua) {
    ua = getRandomUserAgent();
    proxyUaMap.set(proxyKey, ua);
  }
  return ua;
}

export function resetProxyUserAgent(proxyKey: string): void {
  proxyUaMap.delete(proxyKey);
}
